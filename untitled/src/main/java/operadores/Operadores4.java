package operadores;

public class Operadores4 {
    public static void main(String[] args) {
        int numero = 5;
        System.out.println("Usado numero ++ " + numero ++);
        System.out.println("Usado numero " + numero);


        int numero0 = 5;
        System.out.println("Usado numero0 " + ++numero0);
        System.out.println(numero0);

        int numero1 = 5;
        System.out.println("Usado numero1 " + --numero1);
        System.out.println(numero1);

        boolean variavel = true;
        System.out.println(variavel);
        System.out.println(!variavel);
        System.out.println(variavel);

        variavel = !variavel;
        System.out.println(variavel);


    }

}

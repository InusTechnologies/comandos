        int numero01 = 01;

        //A televisão deve aumenar e diminuir o volume
        int volumeAumentando = 7;
        System.out.println("Aumentando o volume estava em 7 e foi para " + ++volumeAumentando);

        int volumeDiminuindo = 7;
        System.out.println("Diminuindo o volume estava em 7 e foi para " + --volumeDiminuindo);




      //A televisão só liga se estive desligada e vice-versa

        boolean simNao = numero00 == numero01;
        System.out.println("tv desligada é igual a tv ligada? " + simNao);

        simNao = numero00 != numero01;
        System.out.println("tv desligada é desigual a tv ligada? " + simNao);















